// Add Two Numbers with JavaScript
let sum = 5 + 10;
console.log(sum); // Output: 15

// Subtract One Number from Another with JavaScript
let difference = 20 - 5;
console.log(difference); // Output: 15

// Multiply Two Numbers with JavaScript
let product = 8 * 4;
console.log(product); // Output: 32

// Divide One Number by Another with JavaScript
let quotient = 24 / 3;
console.log(quotient); // Output: 8

// Increment a Number with JavaScript
let number = 10;
number++;
console.log(number); // Output: 11

// Decrement a Number with JavaScript
let anotherNumber = 15;
anotherNumber--;
console.log(anotherNumber); // Output: 14

// Create Decimal Numbers with JavaScript
let decimalNumber = 3.14;
console.log(decimalNumber); // Output: 3.14

// Multiply Two Decimals with JavaScript
let decimalProduct = 2.5 * 1.5;
console.log(decimalProduct); // Output: 3.75

// Divide One Decimal by Another with JavaScript
let decimalQuotient = 7.5 / 2.5;
console.log(decimalQuotient); // Output: 3

// Finding a Remainder in JavaScript
let remainder = 10 % 3;
console.log(remainder); // Output: 1 (remainder of 10 divided by 3)

// Compound Assignment With Augmented Addition
let x = 5;
x += 3; // Equivalent to x = x + 3;
console.log(x); // Output: 8

// Compound Assignment With Augmented Subtraction
let y = 10;
y -= 4; // Equivalent to y = y - 4;
console.log(y); // Output: 6

// Compound Assignment With Augmented Multiplication
let z = 3;
z *= 6; // Equivalent to z = z * 6;
console.log(z); // Output: 18

// Compound Assignment With Augmented Division
let w = 20;
w /= 5; // Equivalent to w = w / 5;
console.log(w); // Output: 4
